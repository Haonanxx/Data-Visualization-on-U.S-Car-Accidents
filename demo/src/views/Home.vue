<script>

  import * as d3 from 'd3';
  import * as yixiang from '../../public/map.js'
  import * as cland from '../../public/clander.js'
  cland.draw_clander()
  export default {
    name: 'd3Chart',
    data: function () {
      return {
        chartData: null,
      }
    },
    methods: {
      barChart() {
        var svg = d3.select('#chart');
        var sel = svg.selectAll('rect')
                .data(this.chartData)
                .enter();

        sel.append('rect')
                .attr('x', 100)
                .attr('y', (d, i) => 20 + i * 25)
                .attr('width', d => d.value / 1000)
                .attr('class', 'bar');

        sel.append('text')
                .attr('x', 90)
                .attr('y', (d, i) => 30 + i * 25)
                .attr('class', 'label')
                .text(d => d.label);

        sel.append('text')
                .attr('x', d => 90 + d.value / 1000)
                .attr('y', (d, i) => 30 + i * 25)
                .attr('class', 'value')
                .text(d => Number(d.value).toLocaleString());
      }
    },
    mounted: function () {
      console.log('mounted');
      cland.draw_clander()

      var promises = [];

      promises.push(d3.json("counties-albers-10m.json"));
      promises.push(d3.csv("ch_Data.csv"));

      Promise.all(promises).then(function (values) {  //ðŸš§  explain
        yixiang.drawMap(values)
      });

    }
  }
</script>


<template>
  <div class="d3Chart">

    <div class="title">Accidents and Time&Date</div>
    <div class="sub-title">(Data from February 2016 to June 2020)</div>
    <svg width="500" height="500" id="chart"></svg><svg width="500" height="500" id="chart3"></svg>

    <div>
      <div id="calander"></div>
      <svg id="maphead"></svg>
